getJSON.onclick = () => {
  const request = new XMLHttpRequest();
  request.open("GET", "/5.json");
  request.onreadystatechange = () => {
    if (request.readyState === 4) {
      console.log("下载完成");
      if (request.status >= 200 && request.status < 300) {
        const object = JSON.parse(request.response);

        console.log(object);
      }
    }
  };
  request.error = () => {};
  request.send();
};
